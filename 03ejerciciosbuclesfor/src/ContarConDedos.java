public class ContarConDedos {
    public static void main(String[] args) {

        System.out.println("Contaremos del 1 al 10 con los dedos");

        // Simulando contar con los dedos
        for (int dedo = 1; dedo <= 10; dedo++) {
            System.out.println(dedo); // se puede imprimir con algun texto como "1 dedo, 2 dedos, sucesivamente" , permanece igualmente de esta forma
        }
    }
}
